void main() {
// •	If statement

  int n = 10;

  if (n == 10) {
    print("n is equal to 10");
  }
// •	If-else statement

  if (n == 10) {
    print("n is equal to 10");
  } else {
    print("n is not equal to 10");
  }

  // •	(if_else_if) statement.

  if (n == 10) {
    print("n is equal to 10");
  } else if (n < 10) {
    print("n is less then 10");
  } else if (n > 10) {
    print("n is greater then 10");
  } else {
    print("failed");
  }

  // •	Switch statement

  switch (n) {
    case 20:
      print("20");
      break;
    case 50:
      print("10");
      break;
    case 30:
      print("30");
      break;
    case 40:
      print("40");
      break;

    default:
      print("failed");
  }
}



