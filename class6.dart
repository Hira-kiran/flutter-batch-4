void main() {
// •	While loop

  int n = 100;
  while (n < 100) {
    print(n);
    n++;
  }

// •	Do while loop
  do {
    print(n);
    n++;
  } while (n < 10);

  // •	For loop

  for (int i = 0; i < 10; i++) {
    print(i * i);
  }
}

